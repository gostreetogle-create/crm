export type RoleItem = {
  id: string;
  /**
   * Внутренний уникальный код (латиница). В UI не вводится: задаётся при создании из названия, при правке роли не
   * меняется. Матрица прав и сессия завязаны на `id`; `code` — для экспорта/импорта и поля «Код роли» у пользователей.
   * `admin` — только у суперадмина.
   */
  code: string;
  name: string;
  /**
   * Порядок в матрице «Админ-настройки» и в выпадающих списках ролей: **меньше** — левее / выше
   * (например 10 — директор, 20 — бухгалтер, 30 — менеджер). При равенстве — по названию.
   */
  sortOrder: number;
  notes?: string;
  isActive: boolean;
  /** Системная роль одна — суперадмин: нельзя удалить; полный доступ вне матрицы. */
  isSystem?: boolean;
};

export type RoleItemInput = Omit<RoleItem, 'id'>;
