/** Если в справочнике не задана категория — в витрине показываем эту подпись и группируем фильтр по ней. */
export const KP_CATALOG_UNCATEGORIZED_LABEL = 'Без категории';

/** Модель строки каталога для витрины КП; при подключении API замените источник данных, поля можно расширить. */
export type KpCatalogProduct = {
  id: string;
  /** Верхний уровень из справочника «Товары» (или {@link KP_CATALOG_UNCATEGORIZED_LABEL}). */
  category: string;
  /** Уточнение; если в данных пусто — поле можно не задавать. */
  subcategory?: string;
  title: string;
  sku: string;
  /** Цена в рублях (число). */
  price: number;
  /**
   * Прямой URL превью (обязательно с бэкенда: `/media/trade-goods/{артикул}.jpg` и т.п.).
   * Без внешних заглушек — если пусто, карточка без фото.
   */
  imageUrl?: string;
  /**
   * Подпись единицы при добавлении в таблицу КП (по умолчанию в конструкторе — «шт.»).
   * Для справочника «Товары»: часто «компл.» при многопозиционном составе.
   */
  defaultUnit?: string;
  /** Источник строки (демо / справочник товаров) — для отладки и будущей связи с БД КП. */
  source?: 'demo' | 'trade_good';
  /** Тип товара из справочника: товар или комплекс. */
  kind?: 'ITEM' | 'COMPLEX';
  /** Текст из справочника «Товары» → колонка «Описание» в КП. */
  description?: string;
};
